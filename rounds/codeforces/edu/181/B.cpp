const int MOD = 1e9 + 7;
inline ll gcd(ll a, ll b) { while (b != 0) swap(b, a %= b); return a; }

void solve(){
    ll a, b, k; cin >> a >> b >> k;
    ll g = gcd(a, b);
    a /= g; b /= g;

    if(a <= k && b <= k) cout << "1\n";
    else cout << "2\n";
}

signed main() {
    ios_base::sync_with_stdio(0);
    cin.tie(0); cout.tie(0);
    
    int tt; cin >> tt;
    while(tt--) solve();
}